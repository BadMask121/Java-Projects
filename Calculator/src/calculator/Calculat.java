    private void inputKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_inputKeyTyped
        // TODO add your handling code here:
        try{
        char e = evt.getKeyChar();
        
    if(!(Character.isDigit(e)|| (e==KeyEvent.VK_BACK_SPACE)
                || (e==KeyEvent.VK_DELETE)))
        {
        getToolkit().beep();
        evt.consume();
        }
      if(Character.isDigit(e))
        {
        String keys = String.valueOf(evt.getKeyChar());
        input.setText(input.getText()+keys);
        }
        }catch(Exception e){}        
    }//GEN-LAST:event_inputKeyTyped

