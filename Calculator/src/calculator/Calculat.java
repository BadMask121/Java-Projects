    private void equalsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_equalsMouseClicked
        try{
           
        String answer;
   storage = new ArrayList<>();
         secondnumber = Integer.parseInt(input.getText());
         
        if( null != operators)switch (operators) {
                case "+":
                    //input.setText("");
                    result = firstnumber + secondnumber;
                    answer = String.format("%s", result);
                    //          Display.setText("");
                    Display.setText(answer);
                    storage.add(answer);
                    break;
                case "-":
                    // input.setText("");
                    result = firstnumber - secondnumber;
                    answer = String.format("%s", result);
                    //     Display.setText("");
                    Display.setText(answer);
                    storage.add(answer);
                    break;
                case "*":
                    //         input.setText("");
                    result = firstnumber * secondnumber;
                    answer = String.format("%s", result);
                    //    Display.setText("");
                    Display.setText(answer);
                    storage.add(answer);
                    break;
                case "÷":
                    // input.setText("");
                    result = firstnumber / secondnumber ;
                    answer = String.format("%s", result );
                    //     Display.setText("");
                    Display.setText(answer);
                    storage.add(answer);
                    break;
                case "%":
                    // input.setText("");
                    result = firstnumber % secondnumber;
                    answer = String.format("%s", result);
                    //       Display.setText("");
                    Display.setText(answer);
                    storage.add(answer);
                    break;
                default:
                    break;
            }
        }catch(NumberFormatException e)
        {}        

    }//GEN-LAST:event_equalsMouseClicked

